/*
 * 0 : You can't go in there
 * 1 : You can go there, it's free road
 * 2 : You've already been there
 */

using Maze = vector <vector<int>>;

void printSolution(Maze maze) {
    for (auto i = 0; i < maze.size(); ++i) {
        for (auto j = 0; j < maze[i].size(); ++j)
            cout << maze[i][j] << " ";
        cout << endl;
    }
}

bool isPositionAtTheEnd(const Maze &maze, int x, int y) {
    return x == maze.size() - 1 && y == maze[0].size() - 1;
}

bool solveMaze(Maze &maze, int x, int y) {
    bool result = false;

    if (isPositionAtTheEnd(maze, x, y)) {
        maze[x][y] = 2;
        return true;
    }

    maze[x][y] = 2;

    int tempX = x + 1;
    int tempY = y;
    if (tempX < maze.size() && maze[tempX][tempY] == 1)
        result = solveMaze(maze, tempX, tempY);

    tempX = x - 1;
    tempY = y;
    if (!result && tempX >= 0 && maze[tempX][tempY] == 1)
        result = solveMaze(maze, tempX, tempY);

    tempX = x;
    tempY = y + 1;
    if (!result && tempY < maze[0].size() && maze[tempX][tempY] == 1)
        result = solveMaze(maze, tempX, tempY);

    tempX = x;
    tempY = y - 1;
    if (!result && tempY >= 0 && maze[tempX][tempY] == 1)
        result = solveMaze(maze, tempX, tempY);

    return result;
}

